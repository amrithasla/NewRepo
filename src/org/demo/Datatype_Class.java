package org.demo;

public class Datatype_Class {
	
	public static void main(String[] args) {
		
		byte date = 15;
		System.out.println("Employee Date: " +date);
		
		short pinNo = 5432;
		System.out.println("Employee Pin number: " +pinNo);
		
		int regNo = 897437335;
		System.out.println("Employee Register Number: " +regNo);
		
		long phNo = 9876543210l;   // more than 10 digits, we need to mention l
		System.out.println("Employee phone number:" +phNo);
		
		float avg = 48.44f;  // for all float values, we have to mention f
		System.out.println("Employee average : " +avg);
		
		char gender = 'f';
		System.out.println("Employee Gender: " +gender);
		
		double lattitude = 797563.89840;
		System.out.println("Employee Latitue: " +lattitude);
		
		boolean result = true;
		System.out.println("All above informations are " +result);
		
		String email = "Amritha123@gmail.com";
		System.out.println("Employee email address: " +email);
		
				
	}

}
